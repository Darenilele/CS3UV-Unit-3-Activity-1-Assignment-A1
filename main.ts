 // INPUT (Declaring Constants)
const RADIUS: number = 5.6;
const PI: number = 3.14;

// Processing (Calculating Area)
let answer: number = RADIUS * RADIUS * PI

// OUTPUT (Showcasing Results)
console.log("The area of a circle with a radius of " + RADIUS + "cm is " + answer + " cm2")

console.log("\nDone.")